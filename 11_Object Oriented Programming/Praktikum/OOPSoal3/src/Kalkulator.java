package src;

public class Kalkulator {
    public int angka1, angka2;

    public int Penjumlahan(){
        return (this.angka1 + this.angka2);
    }

    public int Pengurangan(){
        return (this.angka1 - this.angka2);
    }

    public int Perkalian(){
        return (this.angka1 * this.angka2);
    }

    public int Pembagian(){
        return (this.angka1 / this.angka2);
    }
}
